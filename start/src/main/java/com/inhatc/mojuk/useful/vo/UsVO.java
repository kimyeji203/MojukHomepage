package com.inhatc.mojuk.useful.vo;

public class UsVO {
	private String us_idx;
	private String us_title;
	private String us_link;
	private String us_writer;
	private String us_date;
	
	public String getUs_idx() {
		return us_idx;
	}
	public void setUs_idx(String us_idx) {
		this.us_idx = us_idx;
	}
	public String getUs_title() {
		return us_title;
	}
	public void setUs_title(String us_title) {
		this.us_title = us_title;
	}
	public String getUs_link() {
		return us_link;
	}
	public void setUs_link(String us_link) {
		this.us_link = us_link;
	}
	public String getUs_writer() {
		return us_writer;
	}
	public void setUs_writer(String us_writer) {
		this.us_writer = us_writer;
	}
	public String getUs_date() {
		return us_date;
	}
	public void setUs_date(String us_date) {
		this.us_date = us_date;
	}
	
}
